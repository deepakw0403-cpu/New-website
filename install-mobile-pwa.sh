# =====================================================================
#  Locofast Mobile PWA — Installer
#  Run inside the DEPLOYED fabric-sourcing-cms (rfq-system-2) workspace.
#
#  Usage (have the agent run this):
#     chmod +x install-mobile-pwa.sh && ./install-mobile-pwa.sh
#
#  What it does:
#    1. Extracts 24 mobile React files into /app/frontend/src/mobile/
#    2. Adds /app/frontend/src/components/TryMobilePreview.js
