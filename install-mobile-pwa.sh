[ -f "$PUBLIC_DIR/index.html"     ] && cp "$PUBLIC_DIR/index.html"     "$BAK/" || true
[ -f "$SRC_DIR/App.js"            ] && cp "$SRC_DIR/App.js"            "$BAK/" || true
